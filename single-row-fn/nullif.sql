	ENAME,
	JOB,
	SAL,
	COMM
FROM
	EMP
WHERE
	EMPNO IN (7839, 7698, 7566, 7654);

SELECT
	ENAME,
	JOB,
	SAL,
	NVL(COMM /*EXPR1*/, 0 /*EXPR1*/ )
FROM
	EMP
WHERE
	EMPNO IN (7839, 7698, 7566, 7654);

SELECT
	ENAME,
	JOB,
	SAL,
	NVL(TO_CHAR(COMM), 'NULL VALUE FOUND')
FROM
	EMP
WHERE
	EMPNO IN (7839, 7698, 7566, 7654);

-- NVL(EXPR1, EXPR2) returns EXPR2 if EXPR1 is NULL
-- NVL returns EXPR1 if it's not NULL

-- NULLIF(arg1, arg2)

SELECT
	ENAME,
	LENGTH(ENAME),
	NULLIF(LENGTH(ENAME), 5)
FROM
	EMP;

-- Show 'String length is 5' if ENAME length is 5
SELECT
	ENAME,
	LENGTH(ENAME),
	NVL(TO_CHAR(NULLIF(LENGTH(ENAME), 5)), 'String length is 5')
FROM
	EMP;

SELECT
	ENAME,
	LENGTH(ENAME),
	NVL(NULLIF(TO_CHAR(LENGTH(ENAME)), TO_CHAR(5)), 'String length is 5')
FROM
	EMP;